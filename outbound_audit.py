def save_baseline(entries, path):
    """Save current traffic patterns as a known-good baseline."""
    destinations = list(set(e["dst"] for e in entries))
    dst_ports = list(set((e["dst"], e["dpt"]) for e in entries if e["dpt"] > 0))

    baseline = {
        "created": datetime.now().isoformat(),
        "entry_count": len(entries),
        "known_destinations": destinations,
        "known_destination_ports": dst_ports,
        "known_ports": list(set(e["dpt"] for e in entries if e["dpt"] > 0)),
    }

    with open(path, "w") as f:
        json.dump(baseline, f, indent=2)

    print(f"\n{C.GREEN}✓ Baseline saved to {path}{C.RESET}")
    print(f"  {len(destinations)} unique destinations, {len(dst_ports)} unique dst:port pairs")
    print(f"  Use with: outbound_audit.py --baseline {path}")


def load_baseline(path: str):
    """Load a previously saved baseline."""
    if not path:
        return None
    try:
        with open(path, "r") as f:
            baseline = json.load(f)
        print(f"  {C.DIM}Loaded baseline from {path} "
              f"({baseline.get('entry_count', '?')} entries, "
              f"created {baseline.get('created', '?')}){C.RESET}")
        return baseline
    except FileNotFoundError:
        print(f"{C.RED}ERROR:{C.RESET} Baseline file not found: {path}")
        sys.exit(1)
    except json.JSONDecodeError:
        print(f"{C.RED}ERROR:{C.RESET} Invalid JSON in baseline: {path}")
        sys.exit(1)


