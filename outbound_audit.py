def print_header(title: str):
    width = 78
    print()
    print(f"{C.BOLD}{C.CYAN}{'═' * width}{C.RESET}")
    print(f"{C.BOLD}{C.CYAN}  {title}{C.RESET}")
    print(f"{C.BOLD}{C.CYAN}{'═' * width}{C.RESET}")


def print_section(title: str):
    print(f"\n{C.BOLD}{C.YELLOW}── {title} {'─' * (74 - len(title))}{C.RESET}")


def print_table(headers, rows, col_widths=None):
    """Print a simple aligned table."""
    if not rows:
        print(f"  {C.DIM}(none){C.RESET}")
        return

    if col_widths is None:
        col_widths = []
        for i, h in enumerate(headers):
            max_w = len(h)
            for row in rows:
                if i < len(row):
                    # Strip ANSI for width calculation
                    clean = re.sub(r"\033\[[0-9;]*m", "", str(row[i]))
                    max_w = max(max_w, len(clean))
            col_widths.append(min(max_w, 60))

    # Header
    header_line = "  "
    for i, h in enumerate(headers):
        header_line += f"{C.BOLD}{h:<{col_widths[i]}}{C.RESET}  "
    print(header_line)
    print(f"  {C.DIM}{'─' * (sum(col_widths) + 2 * len(col_widths))}{C.RESET}")

    # Rows
    for row in rows:
        line = "  "
        for i, cell in enumerate(row):
            clean = re.sub(r"\033\[[0-9;]*m", "", str(cell))
            padding = col_widths[i] - len(clean)
            line += f"{cell}{' ' * max(0, padding)}  "
        print(line)


def port_label(port: int) -> str:
    """Return 'port/SERVICE' or just 'port'."""
    name = KNOWN_PORTS.get(port, "")
    if name:
        return f"{port}/{name}"
    return str(port)


def print_summary_report(entries, top_n, resolve):
    """Print the summary report with top destinations, ports, etc."""
    print_header("OUTBOUND CONNECTION SUMMARY REPORT")

    total = len(entries)
    if total == 0:
        print(f"\n  {C.DIM}No log entries found in the specified range.{C.RESET}")
        return

    ollama_entries = [e for e in entries if e["is_ollama"]]
    blocked_entries = [e for e in entries if e["is_blocked"]]
    allowed_entries = [e for e in entries if not e["is_blocked"]]

    ts_min = min(e["timestamp"] for e in entries)
    ts_max = max(e["timestamp"] for e in entries)

    print(f"\n  {C.BOLD}Time range:{C.RESET}  {ts_min.strftime('%Y-%m-%d %H:%M')} → {ts_max.strftime('%Y-%m-%d %H:%M')}")
    print(f"  {C.BOLD}Total connections:{C.RESET}  {total:,}")
    print(f"  {C.BOLD}Allowed:{C.RESET}  {C.GREEN}{len(allowed_entries):,}{C.RESET}    "
          f"{C.BOLD}Blocked:{C.RESET}  {C.RED}{len(blocked_entries):,}{C.RESET}    "
          f"{C.BOLD}Ollama-originated:{C.RESET}  {C.MAGENTA}{len(ollama_entries):,}{C.RESET}")

    # --- Top destinations ---
    print_section(f"Top {top_n} Destinations")
    dst_counter = Counter(e["dst"] for e in entries)
    rows = []
    for ip, count in dst_counter.most_common(top_n):
        pct = count / total * 100
        bar_len = int(pct / 2)
        bar = f"{C.BLUE}{'█' * bar_len}{C.DIM}{'░' * (25 - bar_len)}{C.RESET}"
        has_ollama = any(e["dst"] == ip and e["is_ollama"] for e in entries)
        flag = f"  {C.BG_RED}{C.WHITE} OLLAMA {C.RESET}" if has_ollama else ""
        rows.append([fmt_dst(ip, resolve), str(count), f"{pct:5.1f}%", bar + flag])
    print_table(["Destination", "Count", "Pct", ""], rows)

    # --- Top ports ---
    print_section(f"Top {top_n} Destination Ports")
    port_counter = Counter(e["dpt"] for e in entries if e["dpt"] > 0)
    rows = []
    for port, count in port_counter.most_common(top_n):
        pct = count / total * 100
        unusual = port not in NORMAL_OUTBOUND_PORTS
        port_str = port_label(port)
        if unusual:
            port_str = f"{C.YELLOW}{port_str}{C.RESET}"
        rows.append([port_str, str(count), f"{pct:5.1f}%"])
    print_table(["Port", "Count", "Pct"], rows)

    # --- Top source IPs (useful if multiple hoppers log to same file) ---
    src_counter = Counter(e["src"] for e in entries)
    if len(src_counter) > 1:
        print_section(f"Top {top_n} Source Hosts")
        rows = []
        for ip, count in src_counter.most_common(top_n):
            rows.append([fmt_dst(ip, resolve), str(count)])
        print_table(["Source", "Count"], rows)

    # --- Hourly distribution ---
    print_section("Hourly Distribution (last 24h)")
    now = datetime.now()
    cutoff_24h = now - timedelta(hours=24)
    recent = [e for e in entries if e["timestamp"] >= cutoff_24h]
    if recent:
        hourly = Counter(e["timestamp"].hour for e in recent)
        max_count = max(hourly.values()) if hourly else 1
        for hour in range(24):
            count = hourly.get(hour, 0)
            bar_len = int(count / max_count * 40) if max_count > 0 else 0
            bar = f"{C.BLUE}{'█' * bar_len}{C.RESET}"
            print(f"  {hour:02d}:00  {bar} {count}")
    else:
        print(f"  {C.DIM}No entries in the last 24 hours.{C.RESET}")

    # --- Protocol breakdown ---
    print_section("Protocol Breakdown")
    proto_counter = Counter(e["proto"] for e in entries if e["proto"])
    rows = [[proto, str(count)] for proto, count in proto_counter.most_common()]
    print_table(["Protocol", "Count"], rows)


def print_ollama_report(entries, resolve):
    """Dedicated section for Ollama activity — the main reason we're here."""
    ollama = [e for e in entries if e["is_ollama"]]

    print_header("OLLAMA OUTBOUND ACTIVITY")

    if not ollama:
        print(f"\n  {C.GREEN}{C.BOLD}✓ No outbound connections from Ollama detected.{C.RESET}")
        print(f"  {C.DIM}This is the expected result if Ollama is behaving.{C.RESET}")
        return

    # This is the interesting case — Ollama is phoning home
    print(f"\n  {C.BG_RED}{C.WHITE}{C.BOLD} ⚠  OLLAMA MADE {len(ollama):,} OUTBOUND CONNECTION(S) {C.RESET}")
    print()

    dst_counter = Counter((e["dst"], e["dpt"]) for e in ollama)
    rows = []
    for (ip, port), count in dst_counter.most_common():
        first_seen = min(e["timestamp"] for e in ollama if e["dst"] == ip and e["dpt"] == port)
        last_seen = max(e["timestamp"] for e in ollama if e["dst"] == ip and e["dpt"] == port)
        rows.append([
            fmt_dst(ip, resolve),
            port_label(port),
            str(count),
            first_seen.strftime("%m-%d %H:%M"),
            last_seen.strftime("%m-%d %H:%M"),
        ])
    print_table(["Destination", "Port", "Count", "First Seen", "Last Seen"], rows)

    print(f"\n  {C.YELLOW}Action items:{C.RESET}")
    print(f"  • Investigate what Ollama is reaching out to")
    print(f"  • Check if OLLAMA_HOST or model pull configs are set")
    print(f"  • Consider blocking with: firewall-cmd --direct --add-rule ipv4 filter OUTPUT 0 \\")
    print(f"      -m owner --uid-owner ollama -j REJECT")


def print_anomaly_report(entries, baseline, resolve):
    """Flag unusual or first-seen activity."""
    print_header("ANOMALY DETECTION")

    anomalies = []

    # --- Unusual ports (not in the normal set) ---
    unusual_port_entries = [e for e in entries if e["dpt"] > 0 and e["dpt"] not in NORMAL_OUTBOUND_PORTS]
    if unusual_port_entries:
        unusual_ports = Counter((e["dst"], e["dpt"]) for e in unusual_port_entries)
        for (ip, port), count in unusual_ports.most_common(20):
            anomalies.append({
                "type": "UNUSUAL_PORT",
                "severity": "MEDIUM",
                "dst": ip,
                "port": port,
                "count": count,
                "detail": f"Port {port_label(port)} is not in the standard outbound set",
            })

    # --- Blocked attempts (something tried to get out and was denied) ---
    blocked = [e for e in entries if e["is_blocked"]]
    if blocked:
        blocked_dsts = Counter((e["dst"], e["dpt"]) for e in blocked)
        for (ip, port), count in blocked_dsts.most_common(20):
            anomalies.append({
                "type": "BLOCKED_ATTEMPT",
                "severity": "HIGH",
                "dst": ip,
                "port": port,
                "count": count,
                "detail": f"Outbound connection was blocked {count} time(s)",
            })

    # --- Ollama outbound (always flagged) ---
    ollama = [e for e in entries if e["is_ollama"]]
    if ollama:
        ollama_dsts = Counter(e["dst"] for e in ollama)
        for ip, count in ollama_dsts.most_common():
            anomalies.append({
                "type": "OLLAMA_OUTBOUND",
                "severity": "CRITICAL",
                "dst": ip,
                "port": 0,
                "count": count,
                "detail": f"Ollama process initiated {count} outbound connection(s)",
            })

    # --- First-seen destinations (vs. baseline) ---
    if baseline:
        known_dsts = set(baseline.get("known_destinations", []))
        known_dst_ports = set(
            tuple(x) for x in baseline.get("known_destination_ports", [])
        )
        current_dsts = set(e["dst"] for e in entries)
        current_dst_ports = set((e["dst"], e["dpt"]) for e in entries if e["dpt"] > 0)

        new_dsts = current_dsts - known_dsts
        new_dst_ports = current_dst_ports - known_dst_ports

        for ip in new_dsts:
            count = sum(1 for e in entries if e["dst"] == ip)
            anomalies.append({
                "type": "NEW_DESTINATION",
                "severity": "HIGH",
                "dst": ip,
                "port": 0,
                "count": count,
                "detail": "Destination NOT in baseline — never seen before",
            })

        for ip, port in new_dst_ports:
            if ip not in new_dsts:  # avoid double-flagging
                count = sum(1 for e in entries if e["dst"] == ip and e["dpt"] == port)
                anomalies.append({
                    "type": "NEW_DST_PORT",
                    "severity": "MEDIUM",
                    "dst": ip,
                    "port": port,
                    "count": count,
                    "detail": f"Known destination, but port {port_label(port)} is new",
                })

    # --- Print anomalies ---
    if not anomalies:
        if baseline:
            print(f"\n  {C.GREEN}{C.BOLD}✓ No anomalies detected vs. baseline.{C.RESET}")
        else:
            print(f"\n  {C.GREEN}{C.BOLD}✓ No obvious anomalies detected.{C.RESET}")
            print(f"  {C.DIM}Tip: Create a baseline with --save-baseline for better detection.{C.RESET}")
        return

    severity_colors = {
        "CRITICAL": C.BG_RED + C.WHITE,
        "HIGH": C.RED,
        "MEDIUM": C.YELLOW,
        "LOW": C.DIM,
    }
    severity_order = {"CRITICAL": 0, "HIGH": 1, "MEDIUM": 2, "LOW": 3}
    anomalies.sort(key=lambda a: (severity_order.get(a["severity"], 99), -a["count"]))

    print(f"\n  Found {C.BOLD}{len(anomalies)}{C.RESET} anomalies:\n")

    rows = []
    for a in anomalies:
        sev = a["severity"]
        sev_str = f"{severity_colors.get(sev, '')}{sev:<8}{C.RESET}"
        dst_str = fmt_dst(a["dst"], resolve)
        port_str = port_label(a["port"]) if a["port"] > 0 else "—"
        rows.append([sev_str, a["type"], dst_str, port_str, str(a["count"]), a["detail"]])
    print_table(["Severity", "Type", "Destination", "Port", "Count", "Detail"], rows)


