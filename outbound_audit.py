def main():
    parser = argparse.ArgumentParser(
        description="Analyze outbound connection logs from firewalld for suspicious activity.",
        formatter_class=argparse.RawDescriptionHelpFormatter,
        epilog="""
Examples:
  sudo python outbound_audit.py                          # Analyze default log
  sudo python outbound_audit.py --ollama-only             # Just Ollama activity
  sudo python outbound_audit.py --save-baseline base.json # Snapshot current traffic
  sudo python outbound_audit.py --baseline base.json      # Compare against baseline
  sudo python outbound_audit.py --after "2025-03-20 08:00" --before "2025-03-20 17:00"
        """,
    )
    parser.add_argument("-f", "--file", default="/var/log/outbound-connections.log",
                        help="Log file to analyze (default: /var/log/outbound-connections.log)")
    parser.add_argument("-n", "--top", type=int, default=10,
                        help="Number of top items to show (default: 10)")
    parser.add_argument("--ollama-only", action="store_true",
                        help="Only show Ollama outbound activity")
    parser.add_argument("--no-resolve", action="store_true",
                        help="Skip reverse DNS lookups (faster)")
    parser.add_argument("--baseline", metavar="FILE",
                        help="Compare against a known-good baseline JSON file")
    parser.add_argument("--save-baseline", metavar="FILE",
                        help="Save current traffic patterns as a baseline and exit")
    parser.add_argument("--after", metavar="DATETIME",
                        help="Only include entries after this time (YYYY-MM-DD HH:MM)")
    parser.add_argument("--before", metavar="DATETIME",
                        help="Only include entries before this time (YYYY-MM-DD HH:MM)")

    args = parser.parse_args()
    resolve = not args.no_resolve

    # Read and parse
    print(f"{C.DIM}Reading {args.file}...{C.RESET}")
    entries = read_log_file(args.file)
    print(f"{C.DIM}Parsed {len(entries):,} log entries.{C.RESET}")

    # Time filters
    if args.after:
        try:
            after_dt = datetime.strptime(args.after, "%Y-%m-%d %H:%M")
            entries = [e for e in entries if e["timestamp"] >= after_dt]
        except ValueError:
            print(f"{C.RED}ERROR:{C.RESET} Invalid --after format. Use 'YYYY-MM-DD HH:MM'")
            sys.exit(1)

    if args.before:
        try:
            before_dt = datetime.strptime(args.before, "%Y-%m-%d %H:%M")
            entries = [e for e in entries if e["timestamp"] <= before_dt]
        except ValueError:
            print(f"{C.RED}ERROR:{C.RESET} Invalid --before format. Use 'YYYY-MM-DD HH:MM'")
            sys.exit(1)

    if not entries:
        print(f"\n{C.YELLOW}No entries found in the specified range.{C.RESET}")
        sys.exit(0)

    print(f"{C.DIM}Analyzing {len(entries):,} entries after filters...{C.RESET}")

    # Save baseline and exit
    if args.save_baseline:
        save_baseline(entries, args.save_baseline)
        sys.exit(0)

    # Load baseline for comparison
    baseline = load_baseline(args.baseline) if args.baseline else None

    # Resolve DNS (batch, with progress)
    if resolve:
        unique_ips = set(e["dst"] for e in entries) | set(e["src"] for e in entries)
        print(f"{C.DIM}Resolving {len(unique_ips)} unique IPs (cached)...{C.RESET}")
        for ip in unique_ips:
            resolve_ip(ip)  # warm the cache

    # Reports
    if args.ollama_only:
        print_ollama_report(entries, resolve)
    else:
        print_summary_report(entries, args.top, resolve)
        print_ollama_report(entries, resolve)
        print_anomaly_report(entries, baseline, resolve)

    # Footer
    print(f"\n{C.DIM}{'─' * 78}{C.RESET}")
    print(f"{C.DIM}Log file: {args.file}{C.RESET}")
    if baseline:
        print(f"{C.DIM}Baseline: {args.baseline}{C.RESET}")
    print()


if __name__ == "__main__":
    main()
